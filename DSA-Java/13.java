
/*
 * Problem :
 * Chef took an examination two times. In the first attempt, he scored X marks
 * while in the second attempt he scored Y marks.
 * According to the rules of the examination, the best score out of the two
 * attempts will be considered as the final score.
 * Input Format
 * The first line contains a single integer T — the number of test cases. Then
 * the test cases follow.
 * The first line of each test case contains two integers X and Y — the marks
 * scored by Chef in the first attempt and
 * second attempt respectively.
 * Output Format
 * For each test case, output the final score of Chef in the examination.
 * Determine the final score of the Chef.
 */
import java.util.*;

public class Main {
    public static void main(String args[]) {
        Scanner scn = new Scanner(System.in);
        int t = scn.nextInt();
        while (t-- > 0) {
            int x = scn.nextInt();
            int y = scn.nextInt();
            System.out.println(Math.max(x, y));
        }
    }
}